# type 함수 : 자료형을 확인할 수 있는 함수
print('type(3) : ', type(3))
print('type(3.0) : ', type(3.0))
print('type("3") : ', type("3"))

print('type("True") : ', type("True"))
print('type(True) : ', type(True))


def hello():
    print("Hello world!")


print("""
def hello():
    print("Hello world!")
""")

print('print(type(hello)) : ', type(hello))
print('print(type(print)) : ', type(print))
