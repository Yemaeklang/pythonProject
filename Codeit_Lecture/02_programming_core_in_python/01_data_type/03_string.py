# 문자열(string) : 키보드로 쓸 수 있는 '문자들'을 표현하는 '자료형'
# 문자열 형태 : '문자열' == "문자열"

print("코드잇")  # 코드잇
print("유재석")  # 유재석

# I'm excited to learn Python!
print("I'm excited to learn Python!")

# I'm excited to learn Python!
print('I\'m excited to learn Python!')

# I'm "excited" to learn Python!
print("I\'m \"excited\" to learn Python!")
# \를 이용하여 문자로 인식 시킨다.

print("Hello" + "World")  # Hello 와 World 를 연결 : HelloWorld
print("Hello" * 3)  # Hello 를 3 번 반복 연결 : HelloHelloHello
print("3" + "5")  # 문자열 3 과 5 를 연결 :  35
