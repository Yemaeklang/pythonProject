# 불 대수 : 진리값('참'과 '거짓'을 표현)
# and 연산, or 연산, not 연산

# 불린 (Boolean)
print("print(True and True) : ", True and True)
print("print(True and False) : ", True and False)
print("print(False and True) : ", False and True)
print("print(False and False) : ", False and False)

print("print(True or True) : ", True or True)
print("print(True or False) : ", True or False)
print("print(False or True) : ", False or True)
print("print(False or False) : ", False or False)

print("print(not True) : ", not True)
print("print(not False) : ", not False)

print("print(2 > 1) : ", 2 > 1)
print("print(2 < 1) : ", 2 < 1)
print("print(3 >= 2) : ", 3 >= 2)
print("print(3 <= 2) : ", 3 <= 2)
print("print(2 == 2) : ", 2 == 2)
print("print(2 != 2) : ", 2 != 2)

print("print(\"Hello\" == \"Hello\") : ", "Hello" == "Hello")
print("print(\"Hello\" != \"Hello\") : ", "Hello" != "Hello")
