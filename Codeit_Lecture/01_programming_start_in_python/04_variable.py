# 변수 : 값을 저장
print("-변수 예시-")
burger_price = 4990
fries_price = 1490
drink_price = 1250

print(burger_price)  # print(4990)
print(burger_price * 2)  # print(4990 * 2)
print(burger_price + fries_price)  # print(4990 + 1490)
print(burger_price * 3 + fries_price * 2 + drink_price * 5)  # print(4990 * 3 + 1490 * 2 + 1250 * 5)
